package com.elcom.eodapp.media.common;

public class eBill {
	private int id;
	private String folio;
	private String itemCode;
	private String IPirce;
	private String IQty;
	private String IAmount;
	private String IStartDate;
	private String IStartTime;
	private String IType;
	private String ISmartCard;
	private String IUnit;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFolio() {
		return folio;
	}
	public void setFolio(String folio) {
		this.folio = folio;
	}
	public String getItemCode() {
		return itemCode;
	}
	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}
	public String getIPirce() {
		return IPirce;
	}
	public void setIPirce(String iPirce) {
		IPirce = iPirce;
	}
	public String getIQty() {
		return IQty;
	}
	public void setIQty(String iQty) {
		IQty = iQty;
	}
	public String getIAmount() {
		return IAmount;
	}
	public void setIAmount(String iAmount) {
		IAmount = iAmount;
	}
	public String getIStartDate() {
		return IStartDate;
	}
	public void setIStartDate(String iStartDate) {
		IStartDate = iStartDate;
	}
	public String getIStartTime() {
		return IStartTime;
	}
	public void setIStartTime(String iStartTime) {
		IStartTime = iStartTime;
	}
	public String getIType() {
		return IType;
	}
	public void setIType(String iType) {
		IType = iType;
	}
	public String getISmartCard() {
		return ISmartCard;
	}
	public void setISmartCard(String iSmartCard) {
		ISmartCard = iSmartCard;
	}
	public String getIUnit() {
		return IUnit;
	}
	public void setIUnit(String iUnit) {
		IUnit = iUnit;
	}
	
	
}
