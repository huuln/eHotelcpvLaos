package com.elcom.eodapp.media.common;

public class eImage {
	private int imageId;
	private String imageName;
	private String imageDef;
	private String imageUrl;
	private String picBg;
	private String picIcon;
	public int getImageId() {
		return imageId;
	}
	public void setImageId(int imageId) {
		this.imageId = imageId;
	}
	public String getImageName() {
		return imageName;
	}
	public void setImageName(String imageName) {
		this.imageName = imageName;
	}
	public String getImageDef() {
		return imageDef;
	}
	public void setImageDef(String imageDef) {
		this.imageDef = imageDef;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public String getPicBg() {
		return picBg;
	}
	public void setPicBg(String picBg) {
		this.picBg = picBg;
	}
	public String getPicIcon() {
		return picIcon;
	}
	public void setPicIcon(String picIcon) {
		this.picIcon = picIcon;
	}
	
	
}
