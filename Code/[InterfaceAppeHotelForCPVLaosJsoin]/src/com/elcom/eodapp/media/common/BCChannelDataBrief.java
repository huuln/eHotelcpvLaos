package com.elcom.eodapp.media.common;

public class BCChannelDataBrief
{
  private short channelID;
  private short ovsChannelID;
  private String channelName;
  private short channelNumber;
  private String price;
  private String url;
  private String urlpic;
  
public short getChannelID() {
	return channelID;
}
public void setChannelID(short channelID) {
	this.channelID = channelID;
}
public short getOvsChannelID() {
	return ovsChannelID;
}
public void setOvsChannelID(short ovsChannelID) {
	this.ovsChannelID = ovsChannelID;
}
public String getChannelName() {
	return channelName;
}
public void setChannelName(String channelName) {
	this.channelName = channelName;
}
public short getChannelNumber() {
	return channelNumber;
}
public void setChannelNumber(short channelNumber) {
	this.channelNumber = channelNumber;
}
public String getPrice() {
	return price;
}
public void setPrice(String price) {
	this.price = price;
}
public String getUrl() {
	return url;
}
public void setUrl(String url) {
	this.url = url;
}
public String getUrlpic() {
	return urlpic;
}
public void setUrlpic(String urlpic) {
	this.urlpic = urlpic;
}

 

}

