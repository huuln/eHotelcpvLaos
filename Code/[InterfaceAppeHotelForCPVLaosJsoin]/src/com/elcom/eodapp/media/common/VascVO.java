package com.elcom.eodapp.media.common;

public class VascVO {
	private String keystb;
	private String ipstb;
	private String vascuser;
	private String vascpass;
	public String getKeystb() {
		return keystb;
	}
	public void setKeystb(String keystb) {
		this.keystb = keystb;
	}
	public String getIpstb() {
		return ipstb;
	}
	public void setIpstb(String ipstb) {
		this.ipstb = ipstb;
	}
	public String getVascuser() {
		return vascuser;
	}
	public void setVascuser(String vascuser) {
		this.vascuser = vascuser;
	}
	public String getVascpass() {
		return vascpass;
	}
	public void setVascpass(String vascpass) {
		this.vascpass = vascpass;
	}
	
	

}
