public class VodURLUnableException extends VodAppException
{

	  public VodURLUnableException()
	  {
	  }

	  public VodURLUnableException(String message)
	  {
		super(message);
	  }

	  public VodURLUnableException(String message, Throwable cause)
	  {
		super(message, cause);
	  }

	  public VodURLUnableException(Throwable cause)
	  {
		super(cause);
	  }
	}
